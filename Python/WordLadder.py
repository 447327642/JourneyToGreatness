import collections

class Solution:
    # @param start, a string
    # @param end, a string
    # @param dict, a set of string
    # @return an integer
    def ladderLength(self, start, end, dict):
        # BFS, generate words (in dict) and add to queue
        n = len(start)
        dict.add(end)
        #ALPHABET = 'abcdefghijklmnopqrstuvwxyz'
        ALPHABET = set()
        for w in dict:
            for c in w:
                ALPHABET.add(c)
        queue = collections.deque([(start, 1)])
        while queue:
            e = queue.popleft()
            #print '->', e,
            curword, curcnt = e[0], e[1]
            if curword == end:
                return curcnt
            for i in xrange(n):
                for w in ALPHABET:
                    if curword[i] == w:
                        continue
                    newword = curword[:i] + w + curword[i+1:]
                    if newword in dict:
                        queue.append((newword, curcnt+1))
                        dict.remove(newword)
        # Not found
        return 0


